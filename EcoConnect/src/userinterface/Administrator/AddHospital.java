    private void addHospitalBtnActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_addHospitalBtnActionPerformed
        // TODO add your handling code here:
        if (txtHosUName.getText().isEmpty() || txtHosPwd.getText().isEmpty() || txtHosName.getText().isEmpty() || txtHosAddr.getText().isEmpty()) {
            JOptionPane.showMessageDialog(this, "Enter all mandatory fields");
        } else {

            if (!txtHosName.getText().matches("[a-zA-Z_]+")) {
                JOptionPane.showMessageDialog(this, "Enter proper name! Name should be string!");
                txtHosName.setText("");
                return;
            }
            else if(!txtHosAddr.getText().matches("\\d+\\s+([a-zA-Z]+|[a-zA-Z]+\\s[a-zA-Z]+)")) {
                JOptionPane.showMessageDialog(this, "Enter proper Address in the format of 123 abcd");
                txtHosAddr.setText("");
                return;
            }
            else if(!txtHosEmail.getText().matches("^(.+)@(.+)$")){
                JOptionPane.showMessageDialog(this, "Enter proper Email in the format of abc@gmail.com");
                txtHosEmail.setText("");
                return;
            }
            else if(!txtHosNum.getText().matches("^\\d{10}$")){
                JOptionPane.showMessageDialog(this, "Enter a 10 digit phone number");
                txtHosNum.setText("");
                return;
            }
            if (strongUsername() == false) {
                txtHosUName.setText("");
                JOptionPane.showMessageDialog(null, "Username should be at least 6 digits and contain at least one upper case letter, one lower case letter, one digit and one special character $, *, # or &.");
                return;
            } else {
                String userName = txtHosUName.getText();
            }
            if (strongPassword() == false) {
                txtHosPwd.setText("");
                JOptionPane.showMessageDialog(null, "Password should be at least 6 digits and contain at least one upper case letter, one lower case letter, one digit and one special character $, *, # or &.");
                return;
            } else {
            }
            
            String name = txtHosName.getText();
            String add = txtHosAddr.getText();
            String username = txtHosUName.getText();
            String pass = txtHosPwd.getText();
            Object selectedItem = comboHosLoc.getSelectedItem();

            String loc = selectedItem.toString();
            Employee emp = business.getEmployeeDirectory().createEmployee(name);
            emp.setLocation(loc);
            
            UserAccount account = business.getUserAccountDirectory().createUserAccount(username, pass, emp, new HospitalAdminRole());
            
            if (business.getEnterpriseDirectory().findEnterprise(name) == null) {
                business.getEnterpriseDirectory().createAndAddEnterprise(name, Enterprise.EnterpriseType.Hospital);
                JOptionPane.showMessageDialog(null, "Hospital created");
                txtHosName.setText("");
                txtHosAddr.setText("");
                txtHosEmail.setText("");
                txtHosNum.setText("");
                txtHosUName.setText("");
                txtHosPwd.setText("");
            } else {
                JOptionPane.showMessageDialog(null, "Hospital already exists");
            }
        }
    }//GEN-LAST:event_addHospitalBtnActionPerformed

