    private void populateTableByLoc() {
        Object selectedItem = comboMngEmeLoc.getSelectedItem();
        String loc = selectedItem.toString();
        DefaultTableModel model = (DefaultTableModel) emerTable.getModel();
        model.setRowCount(0);

        for (Enterprise ins : business.getEnterpriseDirectory().getEnterpriseList()) {
            if (ins.getEnterpriseType().getValue().equals("Emergency") && business.getUserAccountDirectory().findUserAccount(ins.getName()).getEmployee().getLocation().equals(loc)) {
                Object[] row = new Object[2];
                row[0] = ins;

                UserAccount ua = business.getUserAccountDirectory().findUserAccount(ins.getName());
                row[1] = ua.getUsername();
                model.addRow(row);
            }
        }
    }


    private boolean strongUsername() {
        Pattern pat = Pattern.compile("^(?=.*?[A-Z])(?=.*?[a-z])(?=.*?[0-9])(?=.*?[#?!@$%^&*-]).{6,}$");
        Matcher m = pat.matcher(addrTxtFieldEme.getText());
        boolean boo = m.matches();
        return boo;
    }

  
